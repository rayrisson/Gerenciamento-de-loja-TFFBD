    private void buttonDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonDeletarActionPerformed
        if(jtPagamento.getSelectedRow() > (-1)){
            FormaPagamento fp = new FormaPagamento();
            FormaPagamentoDAO fpdao = new FormaPagamentoDAO();
            fp.setId((int) jtPagamento.getValueAt(jtPagamento.getSelectedRow(),0));
            fpdao.delete(fp);
            readTable();
        }else{
            JOptionPane.showMessageDialog(null, "NENHUM CAMPO SELECIONADO");
        }
    }//GEN-LAST:event_buttonDeletarActionPerformed

    private void buttonAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAdicionarActionPerformed
        FormaPagamento fp = new FormaPagamento();
        FormaPagamentoDAO fpdao = new FormaPagamentoDAO();
        fp.setNome(txtNome.getText());
        fpdao.create(fp);
        txtNome.setText("");
        readTable();
    }//GEN-LAST:event_buttonAdicionarActionPerformed

