    public void readTable(){
        DefaultTableModel model = (DefaultTableModel) jtPagamento.getModel();
        jtPagamento.setRowSorter(new TableRowSorter(model));
        model.setNumRows(0);
        FormaPagamentoDAO fpdao = new FormaPagamentoDAO();
        for(FormaPagamento fp : fpdao.read()){
            model.addRow(new Object[]{
                fp.getId(),
                fp.getNome(),
            });
        }
    }
    
